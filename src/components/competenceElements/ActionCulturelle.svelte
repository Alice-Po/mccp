    <h3>1. Compétences de la commune de Putanges-le-Lac</h3>
    <p>La commune a plusieurs leviers pour développer la culture localement :</p>
    
    <h4>1% culturel</h4>
    <p>Insertion d'œuvres d'art dans les constructions publiques (mairie, école, salle des fêtes) : 1% du budget des travaux doit être consacré à une œuvre d'art (sculpture, fresque, installation).</p>
    <p><em>Exemple</em> : Lors de la rénovation de la mairie, la commune pourrait commander une œuvre à un artiste local pour décorer le hall d'entrée.</p>
    
    <h4>Enseignements artistiques</h4>
    <p><strong>École de musique, danse ou théâtre</strong> : La commune peut organiser des cours pour les amateurs, en partenariat avec les écoles ou les associations.</p>
    <p><em>Exemple</em> : Créer un cours de musique pour les enfants, avec des instruments prêtés par la mairie.</p>
    
    <h4>Bibliothèques</h4>
    <p><strong>Bibliothèque municipale</strong> : Gestion des collections, animations (heures du conte, rencontres d'auteurs), et prêt de livres.</p>
    <p><em>Exemple</em> : Organiser un club de lecture ou un festival du livre jeunesse.</p>
    
    <h4>Musées et patrimoine</h4>
    <p><strong>Musées municipaux</strong> : Si Putanges-le-Lac a un musée (sur l'histoire locale ou l'artisanat), elle en assure la gestion et le financement.</p>
    <p><strong>Inventaire du patrimoine</strong> : La commune peut recenser et valoriser son patrimoine (chemins creux, maisons traditionnelles, objets anciens).</p>
    <p><em>Exemple</em> : Créer un parcours patrimonial avec des panneaux explicatifs dans le bourg.</p>
    
    <h4>Archives</h4>
    <p><strong>Conservation des archives municipales</strong> : Classer et mettre en valeur les documents historiques (registres, photos, plans).</p>
    <p><em>Exemple</em> : Numériser les vieux registres paroissiaux pour les rendre accessibles en ligne.</p>
    
    <h4>Archéologie préventive</h4>
    <p><strong>Diagnostics et fouilles</strong> : Si des travaux révèlent des vestiges archéologiques, la commune peut faire appel à un service agréé pour les étudier.</p>
    <p><em>Exemple</em> : Avant de rénover la place du village, vérifier la présence de vestiges médiévaux.</p>

    <h3>2. Compétences de la Communauté de Communes du Val d'Orne (2014)</h3>
    <p>La CdC a choisi de prendre en charge certaines compétences optionnelles en matière culturelle :</p>
    
    <h4>Enseignement musical</h4>
    <p>La CdC peut organiser des cours de musique pour plusieurs communes, ou soutenir une école de musique intercommunale.</p>
    
    <h4>Politique culturelle</h4>
    <p>Mise en place d'une programmation commune (festivals, expositions, résidences d'artistes).</p>
    <p><em>Exemple</em> : Un festival itinérant de musique traditionnelle dans les bourgs de la CdC.</p>
    
    <h4>Soutien aux manifestations culturelles</h4>
    <p>Financement d'événements (Méchoui de la Forêt-Auvray, fêtes locales).</p>
    
    <h4>Centre de Loisirs Sans Hébergement (CLSH)</h4>
    <p>La CdC prend en charge les dépenses de fonctionnement et d'investissement liées aux activités culturelles pour les enfants (ateliers d'arts plastiques pendant les vacances).</p>
    
    <h4>Ce que la CdC ne gère pas (sans extension de compétences) :</h4>
    <p>Les bibliothèques ou musées purement communaux, sauf si la commune lui délègue la gestion.</p>
