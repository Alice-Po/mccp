    <h3>1. Contexte de Putanges-le-Lac</h3>
    <p><strong>Commune rurale</strong> : Putanges-le-Lac fait face à des enjeux typiques des zones rurales (vieillissement, déserts médicaux, fermetures de commerces, déclin démographique).</p>
    
    <h4>Opportunités :</h4>
    <ul>
      <li>Patrimoine naturel et culturel (lac de Rabodanges, chemins creux, bocage).</li>
      <li>Zonage France Ruralités Revitalisation (FRR) : exonérations fiscales et sociales pour attirer des entreprises et des habitants.</li>
    </ul>

    <h3>2. Compétences de la commune de Putanges-le-Lac</h3>
    <h4>Chartes intercommunales de développement et d'aménagement</h4>
    <p>La commune peut participer activement à l'élaboration de chartes intercommunales (revitalisation des bourgs, préservation des paysages).</p>
    <p><em>Exemple</em> : Une charte pour limiter l'artificialisation des sols et encourager la rénovation des logements vacants.</p>
    
    <h4>Contrats et conventions</h4>
    <p><strong>Contrats de Relance et de Transition Écologique (CRTE)</strong> : S'engager avec l'État et la région pour financer des projets locaux.</p>
    <p><strong>Contrats de projet État-Région</strong> : Participer à des contrats pour financer des infrastructures (maison de santé, équipements culturels).</p>
    
    <h4>Zonage France Ruralités Revitalisation (FRR)</h4>
    <p><strong>Exonérations fiscales et sociales</strong> : Accorder des exonérations aux entreprises qui s'installent.</p>
    <p><strong>Majorations de dotations</strong> : Bénéficier d'une dotation de solidarité rurale majorée pour financer des projets locaux.</p>
    
    <h4>Actions locales</h4>
    <p><strong>Revitalisation du centre-bourg</strong> : Créer des espaces de rencontre (café associatif, marché local).</p>
    <p><strong>Soutien aux circuits courts</strong> : Aider les producteurs locaux à vendre directement.</p>
    <p><strong>Lutte contre la vacance</strong> : Inciter à la réhabilitation des logements vacants.</p>

    <h3>3. Compétences de la Communauté de Communes du Val d'Orne</h3>
    <h4>Chartes et contrats intercommunaux</h4>
    <p>La CdC peut piloter des chartes de développement rural (transition écologique, mobilité douce).</p>
    <p>La CdC peut porter un CRTE pour l'ensemble des communes membres.</p>
    
    <h4>Zonage FRR</h4>
    <p><strong>Coordination des exonérations</strong> : Harmoniser les délibérations des communes pour maximiser l'attractivité du territoire.</p>
    <p><strong>Soutien aux entreprises</strong> : Aider les communes à promouvoir les avantages du zonage FRR.</p>
    
    <h4>Aménagement et services</h4>
    <p><strong>Revitalisation des bourgs</strong> : Coordonner des projets de réhabilitation (logements sociaux, espaces publics).</p>
    <p><strong>Mobilité et services</strong> : Développer des solutions de transport (navettes entre bourgs, covoiturage).</p>
    <p><strong>Développement rural</strong> : Soutenir les circuits courts et l'agriculture locale.</p>
    
    <h4>Financements</h4>
    <p><strong>Contrats État-Région</strong> : Négocier des financements pour des projets intercommunaux.</p>
    <p><strong>Fonds européens</strong> : Solliciter des subventions (FEADER pour les projets agricoles, FEDER pour les infrastructures).</p>
