    <h3>1. Compétences de la commune de Putanges-le-Lac</h3>
    <p>La commune a un rôle clé dans l'accueil et la valorisation de son patrimoine local :</p>
    
    <h4>Promotion du territoire</h4>
    <p>Créer des supports de communication (brochures, site internet) pour mettre en avant les atouts de Putanges-le-Lac (lac de Rabodanges, chemins creux, patrimoine bocager).</p>
    
    <h4>Événements locaux</h4>
    <p>Organiser des fêtes ou manifestations (Méchoui de la Forêt-Auvray, marché artisanal) pour attirer des visiteurs.</p>
    
    <h4>Signalétique touristique</h4>
    <p>Installer des panneaux d'information ou des circuits de visite (parcours pédestre autour du lac).</p>
    
    <h4>Accueil des touristes</h4>
    <p>Mettre à disposition des espaces d'information (point d'accueil en mairie, bornes interactives).</p>
    
    <p><em>Exemple concret</em> : La commune pourrait créer un parcours découverte autour du lac de Rabodanges, avec des panneaux expliquant la faune, la flore, et l'histoire locale, en partenariat avec les associations de randonnée.</p>

    <h3>2. Compétences de la Communauté de Communes du Val d'Orne (2014)</h3>
    <p>La CdC a une compétence claire en matière de tourisme et peut agir sur plusieurs leviers :</p>
    
    <h4>Promotion du tourisme</h4>
    <p>Créer une stratégie touristique globale pour le territoire (campagne de communication, site web commun).</p>
    <p><em>Exemple</em> : Une brochure unique présentant toutes les activités de la CdC (lac de Rabodanges, sentiers de randonnée, patrimoine des bourgs).</p>
    
    <h4>Office de tourisme</h4>
    <p>La CdC a déjà créé un office de tourisme à Putanges-Pont-Écrepin (mentionné dans l'arrêté de 2014).</p>
    <p>Elle en assure le fonctionnement et l'investissement (salaires, locaux, outils numériques).</p>
    <p><em>Exemple</em> : L'office peut organiser des visites guidées du lac ou des ateliers de découverte du bocage.</p>
    
    <h4>Aménagement et hébergement</h4>
    <p>Construire ou aménager des équipements touristiques sur ses terrains (aire de pique-nique, location de kayaks sur le lac).</p>
    <p><em>Exemple</em> : Créer un camping municipal ou des gîtes intercommunaux pour accueillir les touristes.</p>
    
    <h4>Entretien des chemins</h4>
    <p>Améliorer et entretenir les sentiers de randonnée (mentionnés dans l'arrêté de 2014).</p>
    <p><em>Exemple</em> : Baliser les chemins creux pour en faire des circuits touristiques.</p>
    
    <h4>Soutien aux communes</h4>
    <p>Aider les communes à organiser des événements ou à valoriser leur patrimoine (subventions pour des fêtes locales).</p>
    
    <h4>Ce que la CdC ne gère pas (sans extension de compétences) :</h4>
    <p>Les hébergements privés (gîtes, chambres d'hôtes) ou les commerces touristiques (restaurants, boutiques), sauf si elle les soutient via des subventions ou des partenariats.</p>
