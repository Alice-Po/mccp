    <h3>1. Rôle de la commune de Putanges-le-Lac</h3>
    <p>La commune a plusieurs leviers pour soutenir l'économie locale, même si ses moyens sont limités :</p>
    
    <h4>Aides aux entreprises</h4>
    <p><strong>Aides à l'immobilier d'entreprises :</strong></p>
    <p>La commune peut accorder des aides (ex : exonérations de taxes, subventions) pour la création ou l'extension d'entreprises sur son territoire (ex : artisan, commerce, exploitation agricole).</p>
    <p><em>Exemple :</em> Aider un jeune agriculteur à reprendre une ferme en lui offrant un local municipal à prix réduit.</p>
    
    <p><strong>Aides aux entreprises en difficulté :</strong></p>
    <p>Participer au financement de dispositifs de soutien (ex : fonds de solidarité local) via une convention avec la région.</p>
    
    <h4>Aides aux commerces et services</h4>
    <p><strong>Maintien des services en milieu rural :</strong></p>
    <p>Subventionner un commerce essentiel (ex : épicerie, boulangerie) ou un service (ex : maison médicale) pour éviter les déserts ruraux.</p>
    <p><em>Exemple :</em> Aider une épicerie multi-services à s'installer en centre-bourg.</p>
    
    <p><strong>Aides aux librairies et salles de cinéma :</strong></p>
    <p>Subventionner une librairie indépendante ou une salle de cinéma itinérante.</p>
    
    <h4>Aides aux professionnels de santé</h4>
    <p><strong>Attractivité médicale :</strong></p>
    <p>La commune peut subventionner l'installation de médecins, infirmiers, ou vétérinaires (pour les élevages locaux).</p>
    <p><em>Exemple :</em> Offrir un local municipal à un médecin en échange d'un engagement à consulter un certain nombre d'heures par semaine.</p>
    
    <h4>Ingénierie financière</h4>
    <p><strong>Garanties d'emprunt :</strong></p>
    <p>Garantir un prêt pour une entreprise locale (ex : un artisan qui veut s'agrandir).</p>
    
    <p><strong>Participation à des fonds d'investissement :</strong></p>
    <p>Participer à des fonds régionaux pour soutenir les entreprises locales (ex : fonds de proximité).</p>
    
    <h4>Rapport annuel</h4>
    <p><strong>Transparence :</strong></p>
    <p>La commune doit transmettre chaque année à la région la liste des aides accordées.</p>

    <h3>2. Rôle de la Communauté de Communes du Val d'Orne (d'après l'arrêté de 2014)</h3>
    <p>La CdC a des compétences obligatoires et optionnelles en matière de développement économique :</p>
    
    <h4>Compétences obligatoires</h4>
    <p><strong>Développement économique :</strong></p>
    <p>Études et actions pour attirer ou soutenir les entreprises (ex : zones d'activités économiques).</p>
    <p><em>Exemple :</em> La CdC gère déjà la zone d'activités économiques de Fromentel (sur La Fresnaye-au-Sauvage) et peut y attirer des entreprises.</p>
    
    <p><strong>Aides indirectes :</strong></p>
    <p>Améliorer l'accueil des entreprises (ex : fibre optique, signalétique).</p>
    
    <h4>Compétences optionnelles exercées (d'après l'arrêté de 2014)</h4>
    <p><strong>Construction et gestion d'équipements :</strong></p>
    <p>La CdC peut construire ou gérer des locaux pour les entreprises (ex : ateliers partagés, pépinière d'entreprises).</p>
    
    <p><strong>Soutien aux projets touristiques :</strong></p>
    <p>Développer des infrastructures liées au tourisme (ex : office de tourisme, hébergements), ce qui peut indirectement soutenir l'économie locale.</p>
    
    <h4>Autres leviers possibles</h4>
    <p><strong>Conventions avec la région :</strong></p>
    <p>La CdC peut signer des conventions pour mettre en œuvre le SRDEII (Schéma Régional de Développement Économique) et cofinancer des aides aux entreprises.</p>
    
    <p><strong>Aides aux organismes de création/reprise d'entreprises :</strong></p>
    <p>Subventionner des structures d'accompagnement (ex : chambre des métiers, couveuses d'entreprises).</p>
    
    <p><strong>Pôle de santé :</strong></p>
    <p>La CdC a la compétence pour créer un pôle de santé libéral et ambulatoire (mentionné dans l'arrêté de 2014), ce qui peut attirer des professionnels de santé et dynamiser l'économie locale.</p>
    
    <p><em>Exemple concret :</em> La CdC pourrait créer une pépinière d'entreprises à Putanges-Pont-Écrepin pour accueillir des artisans ou des start-ups locales, avec des loyers modérés et un accompagnement.</p>
