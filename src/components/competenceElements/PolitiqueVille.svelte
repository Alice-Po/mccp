    <h3>1. Contexte de Putanges-le-Lac</h3>
    <p><strong>Pas de Quartier Prioritaire de la Ville (QPV)</strong> : Putanges-le-Lac, comme la plupart des communes rurales, n'a pas de quartier classé en QPV. Cependant, certaines difficultés sociales (précarité, isolement, manque de services) peuvent justifier des actions inspirées de la Politique de la ville.</p>
    
    <h4>Enjeux locaux :</h4>
    <ul>
      <li>Désertification des services (commerces, santé).</li>
      <li>Vieillissement de la population et isolement des personnes âgées.</li>
      <li>Difficultés d'accès à l'emploi pour les jeunes et les personnes peu qualifiées.</li>
    </ul>

    <h3>2. Rôle de la commune de Putanges-le-Lac</h3>
    <p>Bien que la Politique de la ville soit souvent associée aux zones urbaines, la commune peut s'inspirer de ses outils pour répondre aux enjeux sociaux locaux :</p>
    
    <h4>Actions possibles</h4>
    <p><strong>Diagnostic social</strong> : Identifier les besoins des habitants (accès aux soins, mobilité, logement).</p>
    <p><em>Exemple</em> : Un questionnaire auprès des habitants pour recenser les difficultés (manque de transports, précarité énergétique).</p>
    
    <p><strong>Coordination des acteurs</strong> : Créer un comité local (élus, associations, habitants) pour piloter des projets sociaux (lutte contre l'isolement, accès aux droits).</p>
    
    <p><strong>Mobilité et services</strong> : Organiser des solutions de transport pour les personnes âgées ou sans voiture (navettes vers les commerces ou la maison médicale).</p>
    <p><em>Exemple</em> : Un système de covoiturage solidaire avec des bénévoles.</p>
    
    <p><strong>Accès aux droits</strong> : Mettre en place des permanences sociales (avec la CAF, la MSA) pour aider les habitants à accéder aux aides.</p>
    
    <p><strong>Lien social</strong> : Soutenir les espaces de rencontre (café associatif, ateliers intergénérationnels).</p>
    <p><em>Exemple</em> : Un "café des habitants" mensuel pour discuter des projets du village.</p>
    
    <h4>Financements possibles</h4>
    <p><strong>Contrats locaux</strong> : Même sans QPV, la commune peut signer des conventions avec le département ou la région pour financer des actions sociales (lutte contre la précarité).</p>
    
    <p><strong>Fonds européens</strong> : Solliciter des subventions (FEADER pour les projets ruraux, FSE pour l'insertion).</p>

    <h3>3. Rôle de la Communauté de Communes du Val d'Orne</h3>
    <p>La CdC n'a pas de compétence obligatoire en Politique de la ville, mais elle peut jouer un rôle clé pour coordonner des actions sociales à l'échelle intercommunale :</p>
    
    <h4>Actions possibles</h4>
    <p><strong>Diagnostic intercommunal</strong> : Réaliser une étude sur les besoins sociaux des communes membres (précarité, logement, santé).</p>
    <p><em>Exemple</em> : Un état des lieux des logements vacants pour les transformer en habitats sociaux ou adaptés.</p>
    
    <p><strong>Contrat territorial</strong> : Élaborer un plan d'action intercommunal pour répondre aux enjeux sociaux (maintien des services, insertion).</p>
    <p><em>Exemple</em> : Un "Contrat de cohésion territoriale" avec des objectifs comme la création d'emplois locaux ou l'amélioration des transports.</p>
    
    <p><strong>Mutualisation des services</strong> : Créer des services partagés (un bus intercommunal, une épicerie solidaire).</p>
    <p><em>Exemple</em> : Une plateforme de mobilité pour les habitants sans voiture.</p>
    
    <p><strong>Soutien aux communes</strong> : Aider les petites communes à monter des projets sociaux (subventions, ingénierie).</p>
    <p><em>Exemple</em> : Financer un poste de coordinateur social pour plusieurs bourgs.</p>
    
    <h4>Financements possibles</h4>
    <p><strong>Conventions avec l'État ou la région</strong> : La CdC peut signer des accords pour cofinancer des actions (rénovation de logements, création d'emplois).</p>
    
    <p><strong>Fonds propres</strong> : Utiliser son budget pour des projets sociaux (aides aux associations caritatives).</p>
